package com.yunpay.serv.model;

public class UnionConfig {
	private Integer id;
	private String currMerchNo;
	private String unionMerchId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCurrMerchNo() {
		return currMerchNo;
	}
	public void setCurrMerchNo(String currMerchNo) {
		this.currMerchNo = currMerchNo;
	}
	public String getUnionMerchId() {
		return unionMerchId;
	}
	public void setUnionMerchId(String unionMerchId) {
		this.unionMerchId = unionMerchId;
	}
	
	
}
